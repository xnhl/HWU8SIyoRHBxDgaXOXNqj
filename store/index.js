export const getters = {
	all: state => state.list,
	total: state => state.list.length,
	active: state => state.list.filter(todo => !todo.completed),
	completed: state => state.list.filter(todo => todo.completed),
	activeCount: state => state.list.filter(todo => !todo.completed).length,
	completedCount: state => state.list.filter(todo => todo.completed).length,
	searchResult: (state, word) => state.list.filter(todo => todo.title.indexOf(word) > -1)
}

export const mutations = {
	add: (state, info) => {
		state.list.unshift({
			completed: false,
			id: state.list.length+1,
			title: info.text,
			timestamp: info.timestamp
		})
	},
	replace: (state, todos) => { state.list = todos },
	update: (state, todos) => { state.list = todos },
	remove: (state, todo) => { state.list = state.list.filter(each => each.id !== todo.id) },
	toggle: (state, todo) => {
		let x = state.list.filter(each => each.id == todo.id)[0]
		x.completed = !x.completed
	},
	removeCompleted: state => {
		let completed = state.list.filter(each => each.completed)
		if (completed.length) {
			state.list = state.list.filter(each => !each.completed)
		} else state.list = []
	},
	sortAlpha: (state, reverse = false) => {
		let sorted = state.list.sort((a, b) => a.title < b.title ? -1 : b.title > a.title ? 1 : 0)
		state.list = reverse ? sorted.reverse() : sorted
	},
	sortLength: (state, reverse = false) => {
		let sorted = state.list.sort((a, b) => a.title.length - b.title.length)
		state.list = reverse ? sorted.reverse() : sorted
	},
	sortCompleted: (state, reverse = false) => {
		let done = state.list.filter(each => each.completed)
		let notDone = state.list.filter(each => !each.completed)
		state.list = reverse ? notDone.concat(done) : done.concat(notDone)
	}
}

export const actions = {
	add: ({ commit }, todo) => { commit('add', todo) },
	remove: ({ commit }, todo) => { commit('remove', todo) },
	toggle: ({ commit }, todo) => { commit('toggle', todo) },
	replace: ({ commit }, todos) => { commit('replace', todos) },
	update: ({ commit }, todos) => { commit('update', todos) },
	removeCompleted: ({ commit }) => { commit('removeCompleted') },
	sortAlpha: ({ commit }, reverse = false) => { commit('sortAlpha', reverse) },
	sortLength: ({ commit }, reverse = false) => { commit('sortLength', reverse) },
	sortCompleted: ({ commit }, reverse = false) => { commit('sortCompleted', reverse) }
}
